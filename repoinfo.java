
public class repoinfo {

	
	String id;
	String url;

	
	public repoinfo(String id,String url) {
		super();
		this.id = id;
		this.url = url;
	}


	 public String getId() {   
	        return id;   
	 }  
	 
	 public String getUrl() {   
	        return url;   
	 }  

	
	
	
	
}
