
public class staruserinfo {

	String id;
	String name;
	String time;
	
	public staruserinfo(String id,String name,String time) {
		super();
		this.id = id;
		this.name = name;
		this.time=time;
	}


	 public String getId() {   
	        return id;   
	 }  
	 
	 public String getName() {   
	        return name;   
	 }  
	 public String getTime() {   
	        return time;   
	 }
	
	
	
}
